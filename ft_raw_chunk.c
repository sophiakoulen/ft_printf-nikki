#include "ft_printf.h"

size_t	ft_raw_chunk(const char **fmt)
{
	size_t	i;

	i = 0;
	while ((*fmt)[i] != '\0' && (*fmt)[i] != '%')
		i++;
	write(1, *fmt, i);
	*fmt += i;
	return (i);
}